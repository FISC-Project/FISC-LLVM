#ifndef FISCSUBTARGET_H
#define FISCSUBTARGET_H

#include "FISC.h"
#include "FISCFrameLowering.h"
#include "FISCISelLowering.h"
#include "FISCInstrInfo.h"
#include "FISCSelectionDAGInfo.h"
#include "FISCSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "FISCGenSubtargetInfo.inc"

namespace llvm {

class StringRef;

class FISCSubtarget : public FISCGenSubtargetInfo {
	virtual void anchor();

private:
	const DataLayout DL;       // Calculates type size & alignment.
	FISCInstrInfo InstrInfo;
	FISCTargetLowering TLInfo;
	FISCSelectionDAGInfo TSInfo;
	FISCFrameLowering FrameLowering;
	InstrItineraryData InstrItins;

public:
	/// This constructor initializes the data members to match that
	/// of the specified triple.
	///
	FISCSubtarget(const Triple &TT, StringRef CPU, StringRef FS, FISCTargetMachine &TM);

	/// getInstrItins - Return the instruction itineraries based on subtarget
	/// selection.
	const InstrItineraryData *getInstrItineraryData() const override {
		return &InstrItins;
	}

	const FISCInstrInfo *getInstrInfo() const override { 
		return &InstrInfo; 
	}

	const FISCRegisterInfo *getRegisterInfo() const override {
		return &InstrInfo.getRegisterInfo();
	}

	const FISCTargetLowering *getTargetLowering() const override {
		return &TLInfo;
	}

	const FISCFrameLowering *getFrameLowering() const override {
		return &FrameLowering;
	}

	const FISCSelectionDAGInfo *getSelectionDAGInfo() const override {
		return &TSInfo;
	}

	/// ParseSubtargetFeatures - Parses features string setting specified
	/// subtarget options.  Definition of function is auto generated by tblgen.
	void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace

#endif

